@mixin  bs-multiple-carousel-item-repeater($count) {
  $result-selector: "";

  //main loop
  <?php while: ?> $count > 0 {
    $result-selector: $result-selector + "+ .carousel-item";
    $count: $count - 1;

    //secondary loop
    $nested-count: $count;
    <?php while: ?> $nested-count > 0 {
      $result-selector: $result-selector + "+ .carousel-item";
      $nested-count: $nested-count - 1;
    }

    <?php if: ?> $count != 0 {
      $result-selector: $result-selector + ",";
    }
  }

  #{$result-selector} {
    @content;
  }
}

